# ============================================================
# 各種超限類型
# ============================================================

BREACH_TYPE_MAPPING = {

    # report.py中的超限欄位名稱：
    # 顯示在通知中的中文名稱

    "dv01_breach": "DV01超限",

    "monthly_stop_loss_breach": "月停損超限",

    "yearly_stop_loss_breach": "年停損超限"
}


# ============================================================
# 根據超限類型取得規章
# ============================================================

def get_breach_rule(breach_type):

    # 根據breach_type找出中文超限名稱
    breach_name = BREACH_TYPE_MAPPING.get(
        breach_type
    )

    # 如果找不到這個超限類型
    if breach_name is None:

        # 回傳None，表示需要人工確認
        return None

    # 複製共用規章內容
    # 使用copy避免修改到原本的COMMON_BREACH_RULE
    rule = COMMON_BREACH_RULE.copy()

    # 將本次超限項目名稱加入結果
    rule["breach_type"] = breach_type
    rule["breach_name"] = breach_name

    # 回傳完整規章資料
    return rule


